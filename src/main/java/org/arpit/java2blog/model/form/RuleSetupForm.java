package org.arpit.java2blog.model.form;

/**
 * @author raghav.rampal
 *
 */
public class RuleSetupForm  {

	private String ruleNumber;
	private String ruleName;
	private String account;
	private String fc;
	private String dgp;
	private String accountType;
	private String discount;
	private String terms;
	private boolean overridenExplicitly;
	private boolean hardcode;
	private String priority;
	private String combo;
	private String isbn;
	
	public String getRuleNumber() {
		return ruleNumber;
	}
	public void setRuleNumber(String ruleNumber) {
		this.ruleNumber = ruleNumber;
	}
	public String getRuleName() {
		return ruleName;
	}
	public void setRuleName(String ruleName) {
		this.ruleName = ruleName;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getFc() {
		return fc;
	}
	public void setFc(String fc) {
		this.fc = fc;
	}
	public String getDgp() {
		return dgp;
	}
	public void setDgp(String dgp) {
		this.dgp = dgp;
	}
	public String getAccountType() {
		return accountType;
	}
	public void setAccountType(String accountType) {
		this.accountType = accountType;
	}
	public String getDiscount() {
		return discount;
	}
	public void setDiscount(String discount) {
		this.discount = discount;
	}
	public String getTerms() {
		return terms;
	}
	public void setTerms(String terms) {
		this.terms = terms;
	}
	public boolean isOverridenExplicitly() {
		return overridenExplicitly;
	}
	public void setOverridenExplicitly(boolean overridenExplicitly) {
		this.overridenExplicitly = overridenExplicitly;
	}
	public boolean isHardcode() {
		return hardcode;
	}
	public void setHardcode(boolean hardcode) {
		this.hardcode = hardcode;
	}
	public String getPriority() {
		return priority;
	}
	public void setPriority(String priority) {
		this.priority = priority;
	}
	public String getCombo() {
		return combo;
	}
	public void setCombo(String combo) {
		this.combo = combo;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	
	
	

}
